#!/usr/bin/env bash
# ---------------------------------------------------------------------------
# FreeRADIUS + daloRADIUS add-on entrypoint.
#
# Design goals:
#   1. No s6-overlay, no supervisord — just bash + tini as PID 1.
#   2. Start Apache (background) and FreeRADIUS (background), then `wait -n`.
#      If either dies the container exits and HA restarts it.
#   3. Trap SIGTERM/SIGINT so HA "stop" is clean.
#   4. No privileged /tmp work; SQLite DB lives in /data (persistent volume).
# ---------------------------------------------------------------------------
set -euo pipefail

CONFIG_PATH="/data/options.json"
DATA_DIR="/data"
DB_PATH="${DATA_DIR}/radius.db"

log() { echo "[run.sh] $*"; }

# -------------------------------------------------------------------
# 1. Read user-supplied options
# -------------------------------------------------------------------
if [ ! -f "${CONFIG_PATH}" ]; then
    log "ERROR: ${CONFIG_PATH} not found — is this running inside Home Assistant?"
    exit 1
fi

RADIUS_SECRET="$(jq -r '.radius_shared_secret // "testing123"' "${CONFIG_PATH}")"
DALO_ADMIN_PW="$(jq -r '.daloradius_admin_password // "radius"' "${CONFIG_PATH}")"
LOG_LEVEL="$(jq -r '.log_level // "info"' "${CONFIG_PATH}")"

case "${LOG_LEVEL}" in
    debug)   RADIUS_FLAGS="-X" ;;        # full debug
    info)    RADIUS_FLAGS="-f -l stdout" ;;
    warning) RADIUS_FLAGS="-f -l stdout" ;;
    error)   RADIUS_FLAGS="-f -l stdout" ;;
    *)       RADIUS_FLAGS="-f -l stdout" ;;
esac

# -------------------------------------------------------------------
# 2. First-run: initialise SQLite DB
# -------------------------------------------------------------------
mkdir -p "${DATA_DIR}"

if [ ! -f "${DB_PATH}" ]; then
    log "First run — creating SQLite schema at ${DB_PATH}"

    # FreeRADIUS-shipped schema for radcheck/radreply/radacct/...
    FR_SCHEMA="/etc/freeradius/3.0/mods-config/sql/main/sqlite/schema.sql"
    if [ -f "${FR_SCHEMA}" ]; then
        sqlite3 "${DB_PATH}" < "${FR_SCHEMA}"
    else
        log "WARNING: FreeRADIUS SQLite schema not found at ${FR_SCHEMA}"
    fi

    # daloRADIUS additional tables (operators, billing, hotspot, ...).
    # daloRADIUS 2.x ships a combined SQLite schema under contrib/db.
    DALO_SCHEMA_DIR="/var/www/daloradius/contrib/db"
    for candidate in \
        "${DALO_SCHEMA_DIR}/fr3-sqlite-schema.sql" \
        "${DALO_SCHEMA_DIR}/fr3-sqlite-daloradius-and-freeradius.sql" \
        "${DALO_SCHEMA_DIR}/sqlite-daloradius.sql" ; do
        if [ -f "${candidate}" ]; then
            log "Applying daloRADIUS schema: ${candidate}"
            sqlite3 "${DB_PATH}" < "${candidate}" || true
            break
        fi
    done

    # Default operator account (username: administrator, pw: from options)
    # daloRADIUS hashes with MD5 for the operators table in 2.x.
    PW_HASH="$(printf '%s' "${DALO_ADMIN_PW}" | md5sum | awk '{print $1}')"
    sqlite3 "${DB_PATH}" <<SQL || true
CREATE TABLE IF NOT EXISTS operators (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    username VARCHAR(32) NOT NULL UNIQUE,
    password VARCHAR(32) NOT NULL,
    firstname VARCHAR(32),
    lastname  VARCHAR(32),
    title     VARCHAR(32),
    department VARCHAR(32),
    company   VARCHAR(32),
    phone1    VARCHAR(32),
    phone2    VARCHAR(32),
    email1    VARCHAR(32),
    email2    VARCHAR(32),
    messenger1 VARCHAR(32),
    messenger2 VARCHAR(32),
    notes     TEXT,
    lastlogin DATETIME,
    creationdate DATETIME,
    creationby VARCHAR(32),
    updatedate DATETIME,
    updateby VARCHAR(32)
);
INSERT OR REPLACE INTO operators (username, password, firstname, lastname, creationdate, creationby)
VALUES ('administrator', '${PW_HASH}', 'Admin', 'Istrator', datetime('now'), 'system');
SQL

    chown freerad:www-data "${DB_PATH}"
    chmod 660 "${DB_PATH}"
    log "Database initialised"
fi

# Always enforce permissions (handles volume restore / user swap)
chown freerad:www-data "${DB_PATH}" || true
chmod 660 "${DB_PATH}" || true
chmod 770 "${DATA_DIR}" || true

# -------------------------------------------------------------------
# 3. Write daloRADIUS runtime config to point at our SQLite DB
# -------------------------------------------------------------------
DALO_CONF="/var/www/daloradius/app/common/includes/daloradius.conf.php"
mkdir -p "$(dirname "${DALO_CONF}")"
cat > "${DALO_CONF}" <<EOF
<?php
// Auto-generated by /usr/local/bin/run.sh on container start. Do not edit by hand.
\$configValues['FREERADIUS_VERSION']          = '3';
\$configValues['CONFIG_DB_ENGINE']            = 'sqlite';
\$configValues['CONFIG_DB_HOST']              = '';
\$configValues['CONFIG_DB_PORT']              = '';
\$configValues['CONFIG_DB_USER']              = '';
\$configValues['CONFIG_DB_PASS']              = '';
\$configValues['CONFIG_DB_NAME']              = '${DB_PATH}';
\$configValues['CONFIG_DB_TBL_RADCHECK']      = 'radcheck';
\$configValues['CONFIG_DB_TBL_RADREPLY']      = 'radreply';
\$configValues['CONFIG_DB_TBL_RADGROUPCHECK'] = 'radgroupcheck';
\$configValues['CONFIG_DB_TBL_RADGROUPREPLY'] = 'radgroupreply';
\$configValues['CONFIG_DB_TBL_RADUSERGROUP']  = 'radusergroup';
\$configValues['CONFIG_DB_TBL_RADACCT']       = 'radacct';
\$configValues['CONFIG_DB_TBL_RADPOSTAUTH']   = 'radpostauth';
\$configValues['CONFIG_DB_TBL_RADNAS']        = 'nas';
\$configValues['CONFIG_IFACE_PASSWORD_HASH']  = 'cleartext';
\$configValues['CONFIG_LOG_FILE']             = '/tmp/daloradius.log';
EOF
chown www-data:www-data "${DALO_CONF}"
chmod 640 "${DALO_CONF}"

# -------------------------------------------------------------------
# 4. Inject RADIUS shared secret for the default client (127.0.0.1)
# -------------------------------------------------------------------
CLIENTS="/etc/freeradius/3.0/clients.conf"
if [ -f "${CLIENTS}" ]; then
    # Replace the default "testing123" secret in the shipped client stanza.
    sed -i "s/^\(\s*secret\s*=\s*\).*/\1${RADIUS_SECRET}/" "${CLIENTS}"
fi

# -------------------------------------------------------------------
# 5. Start services with signal handling
# -------------------------------------------------------------------
APACHE_PID=""
RADIUS_PID=""

shutdown() {
    log "SIGTERM received — stopping services"
    [ -n "${APACHE_PID}" ] && kill -TERM "${APACHE_PID}" 2>/dev/null || true
    [ -n "${RADIUS_PID}" ] && kill -TERM "${RADIUS_PID}" 2>/dev/null || true
    wait 2>/dev/null || true
    exit 0
}
trap shutdown SIGTERM SIGINT

log "Starting Apache2 on :80"
# shellcheck disable=SC1091
. /etc/apache2/envvars
apache2 -DFOREGROUND &
APACHE_PID=$!

log "Starting FreeRADIUS (flags: ${RADIUS_FLAGS})"
# shellcheck disable=SC2086
freeradius ${RADIUS_FLAGS} &
RADIUS_PID=$!

log "Both services up — Apache PID=${APACHE_PID}, FreeRADIUS PID=${RADIUS_PID}"

# Wait for either to exit; propagate its exit code so HA restarts us.
set +e
wait -n "${APACHE_PID}" "${RADIUS_PID}"
EXIT_CODE=$?
log "A service exited (code ${EXIT_CODE}); terminating the other and exiting"
shutdown
